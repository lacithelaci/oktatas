"""
Integer (egész szám): Ezek az egész számokat jelölik, például 5, -10, 1000.
Float (lebegőpontos szám): Ezek tizedesjegyeket tartalmazó számok, például 3.14, 2.5, 0.001.
String (karakterlánc): Szövegeket tárol, például 'hello', "Python", '123'.
Boolean (logikai érték): Igaz vagy Hamis értéket tárol, azaz True vagy False.
"""

x = 5
y = 3.14
z = "hello"
w = True

print(type(x))  # <class 'int'>
print(type(y))  # <class 'float'>
print(type(z))  # <class 'str'>
print(type(w))  # <class 'bool'>
